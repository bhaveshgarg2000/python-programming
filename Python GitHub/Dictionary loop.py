dict = {
    "Model":"XUV",
    "Price":150000.00,
    "Safety":True,
    "Features":"Best",
    "Year":2022
}

print("Keys")
for y in dict.keys():
    print(y)
print("\n")

print("Values")
for x in dict.values():
    print(x)
print('\n')
print("Items : Dict as tuples")
print('\n')
for z in dict.items():
    print(z)
